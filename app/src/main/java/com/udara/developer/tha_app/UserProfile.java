package com.udara.developer.tha_app;

public class UserProfile {
    String index,
            name,
            email,
            mobile,
            gpa;

    public UserProfile(String index, String name, String email, String mobile, String gpa) {
        this.index = index;
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.gpa = gpa;
    }
}
